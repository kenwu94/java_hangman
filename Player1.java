    public static boolean word_check(String word) {
        // System.out.println(word);
        try{
            try ( // creates a BufferedReader to quickly read through the dictionary file
                    BufferedReader in = new BufferedReader(new FileReader("dictionary.txt"))) {
                String str;
                // loops through the list of words in the dictionary file
                while ((str = in.readLine()) != null){
                    // if the file contains the word, returns true
                    if (str.contains(word)){
                        return true;
                    }
                }
            }
        } 
        catch (IOException e) {
        }
        // returns false if the file does not contain the word
        return false;
    }
    
