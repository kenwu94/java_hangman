    private void p1Start_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_p1Start_btnActionPerformed
        // TODO add your handling code here:
        if(p1Word_txt.getText().isEmpty()){
            // displays error telling user that they did not enter a word
            JOptionPane.showMessageDialog(rootPane, "Word input missing.", "Error", HEIGHT);
        }
        else{
            // gets the word from player1 input
            word = p1Word_txt.getText().toLowerCase();
            // checks if the word is a real word
            if(!word_check(word)){
                // displays error telling user that the word does not exist
                JOptionPane.showMessageDialog(rootPane, "Word does not exist in our database.", "Error", HEIGHT);
            }
            else{
                // creates an array of the length of the word which will store each individual letter
                letters = new String[word.length()];
                // loops through each character in the word and stores it in the array, forcing each letter to be lowercase
                for(int i = 0; i < word.length(); i++){
                    letters[i] = String.valueOf(word.charAt(i));
                }

                // checks how many guesses player1 wants to give
                if(numGuesses_7box.isSelected()){
                    numGuesses = 7;
                }
                else if(numGuesses_8box.isSelected()){
                    numGuesses = 8;
                }
                else if(numGuesses_9box.isSelected()){
                    numGuesses = 9;
                }
                else if(numGuesses_10box.isSelected()){
                    numGuesses = 10;
                }
                // defaults to 6 guesses
                else{
                    numGuesses = 6;
                }

                // opens Player2 jFrame form
                this.setVisible(false);
                new Player2().setVisible(true);
            }
        }
    }//GEN-LAST:event_p1Start_btnActionPerformed

